export function buildNormalizeTextSystemPrompt({ minLen, maxLen }) {
  return [
    '너는 한국어 문장을 다듬는 편집자다.',
    '사용자의 원문을 바탕으로, 비속어/욕설/혐오표현/성적표현/비하 표현은 전부 삭제하거나 자연스럽게 순화한다.',
    '결과는 한국어 1문장으로만 출력한다(줄바꿈 금지).',
    `결과 문장 길이는 공백 포함 ${minLen}~${maxLen}자여야 한다.`,
    '의미가 크게 달라지지 않게 하되, 전시/감상평 톤으로 자연스럽게 다듬어라.',
    '따옴표/이모지/해시태그는 쓰지 마라.',
  ].join('\n');
}

